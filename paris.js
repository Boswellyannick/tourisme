function previous() {
    const widthSlider = document.querySelector('.left').offsetWidth;
    document.querySelector('.image__content').scrollLeft -= widthSlider;

}

function next() {
    const widthSlider = document.querySelector('.left').offsetWidth;
    document.querySelector('.image__content').scrollLeft += widthSlider;
}

